public class TipoJustificante {
    private String IDjustificante;
    private String descripcionJustificante;
    private String statusJustificante;


    public String getIDjustificante() {
        return IDjustificante;
    }

    public void setIDjustificante(String IDjustificante) {
        this.IDjustificante = IDjustificante;
    }

    public String getDescripcionJustificante() {
        return descripcionJustificante;
    }

    public void setDescripcionJustificante(String descripcionJustificante) {
        this.descripcionJustificante = descripcionJustificante;
    }

    public String getStatusJustificante() {
        return statusJustificante;
    }

    public void setStatusJustificante(String statusJustificante) {
        this.statusJustificante = statusJustificante;
    }
}
