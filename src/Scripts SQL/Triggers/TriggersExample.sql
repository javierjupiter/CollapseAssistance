USE DBREGASI;

DELIMITER ||

DROP TRIGGER IF EXISTS TR_BIDDATEMP ||
CREATE TRIGGER TR_BIDDATEMP BEFORE INSERT ON DDATEMP
FOR EACH ROW
BEGIN
SET NEW.CUSUINS = SUBSTRING(CURRENT_USER(),1,10);
SET NEW.DFECINS = CURRENT_DATE();
END

||

DELIMITER ||

DROP TRIGGER IF EXISTS TR_BUDDATEMP ||
CREATE TRIGGER TR_BUDDATEMP BEFORE UPDATE ON DDATEMP
FOR EACH ROW
BEGIN
SET NEW.CUSUMOD = SUBSTRING(CURRENT_USER(),1,10);
SET NEW.DFECMOD = CURRENT_DATE();
END

||


