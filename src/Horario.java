public class Horario {
    private String ID_Horario;
    private String Descripcion_Horario;
    private String Status_Horario;


    public String getID_Horario() {
        return ID_Horario;
    }

    public void setID_Horario(String ID_Horario) {
        this.ID_Horario = ID_Horario;
    }

    public String getDescripcion_Horario() {
        return Descripcion_Horario;
    }

    public void setDescripcion_Horario(String descripcion_Horario) {
        Descripcion_Horario = descripcion_Horario;
    }

    public String getStatus_Horario() {
        return Status_Horario;
    }

    public void setStatus_Horario(String status_Horario) {
        Status_Horario = status_Horario;
    }
}
