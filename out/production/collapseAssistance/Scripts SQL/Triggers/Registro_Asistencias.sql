USE CollapseAssistance; /*Vanessa*/

DELIMITER ||

DROP TRIGGER IF EXISTS TRIGGER_BEFORE_INSERT_Registro_Asistencias ||
CREATE TRIGGER TRIGGER_BEFORE_INSERT_R BEFORE INSERT ON Registro_Asistencias
FOR EACH ROW
BEGIN
SET NEW.Inserto_Usuario = SUBSTRING(CURRENT_USER(),1,10);
SET NEW.Fecha_Insercion = CURRENT_DATE();
END

||

DELIMITER ||

DROP TRIGGER IF EXISTS TRIGGER_BEFORE_UPDATE_Registro_Asistencias ||
CREATE TRIGGER TRIGGER_BEFORE_UPDATE_Registro_Asistencias BEFORE UPDATE ON Registro_Asistencias
FOR EACH ROW
BEGIN
SET NEW.Modifico_Usuario = SUBSTRING(CURRENT_USER(),1,10);
SET NEW.Fecha_Modificacion = CURRENT_DATE();
END

||
